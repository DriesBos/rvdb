<template>
  <section class="view view-Profiel">
    <blok-anchor-nav :input="anchorList" />
    <div class="view-NavPlaceholder">
      <div>
        <p></p>
      </div>
    </div>
    <component
      :is="story.content.component | dashify"
      v-if="story.content.component"
      :key="story.content._uid"
      :blok="story.content"
      class="view-Content"
    />
  </section>
</template>

<script>
import storyblokLivePreview from "@/mixins/storyblokLivePreview"

export default {
  mixins: [storyblokLivePreview],
  asyncData(context) {
    let version =
      context.query._storyblok || context.isDev ? "draft" : "published"
    return context.app.$storyapi
      .get("cdn/stories/profiel", {
        version: version
      })
      .then(res => {
        return res.data
      })
      .catch(res => {
        if (!res.response) {
          console.error(res)
          context.error({
            statusCode: 404,
            message: "Failed to receive content form api"
          })
        } else {
          console.error(res.response.data)
          context.error({
            statusCode: res.response.status,
            message: res.response.data
          })
        }
      })
  },
  data() {
    return {
      story: { content: {} },
      anchorList: {}
    }
  },
  mounted() {
    // console.log("PROFIEL", this.story.content.body)
    this.sortAnchorList()
  },
  methods: {
    sortAnchorList() {
      let array = this.story.content.body
      let filteredList = array.filter(function(el) {
        if (el.title !== "") {
          return true
        }
      })
      this.anchorList = filteredList
    }
  }
}
</script>
